# syntax of while:-
# while<condition>
# as long as condition is true the code will be executed
# condition false. the loop terminates

# for i in range (10):
#     print("i is now {}".format(i))


i = 0                   # in while loop we have to assign value of i, but in for loop we don't, it automaticlly fetch values from the range
while i <= 10:
    print("i is now {}" .format(i))
    i += 1                          # if we remove indentation the condition will become false & the loop will keep running forever









